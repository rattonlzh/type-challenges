type Str2Union<S> = S extends `${infer F}${infer R}` ? F | Str2Union<R> : never
type DropString<S, R> = '' extends R ? S : S extends `${infer F}${infer Rest}` ? F extends Str2Union<R> ? DropString<Rest, R> : `${F}${DropString<Rest, R>}` : S
